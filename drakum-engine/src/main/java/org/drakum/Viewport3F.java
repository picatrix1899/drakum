package org.drakum;

public class Viewport3F
{
	public float posX;
	public float posY;
	public float posZ;
	public float width;
	public float height;
	public float depth;
	
	public Viewport3F(float posX, float posY, float posZ, float width, float height, float depth)
	{
		this.posX = posX;
		this.posY = posY;
		this.posZ = posZ;
		this.width = width;
		this.height = height;
		this.depth = depth;
	}
}
